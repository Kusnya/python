# Задача-1: Дано произвольное целое число, вывести самую большую цифру этого числа.
# Например, дается x = 58375.
# Нужно вывести максимальную цифру в данном числе, т.е. 8.
# Подразумевается, что мы не знаем это число заранее.
# Число приходит в виде целого беззнакового.
# Подсказки:
# * постарайтесь решить задачу с применением арифметики и цикла while;
# * при желании и понимании решите задачу с применением цикла for

print()
print("Задача-1: Вывести наибольшую цифру числа.")
print("Решение Задачи-1 при помощи цикла while")
print()

num = int(input("Введите число: "))

max = 0

while num > 0:
    reminder = num % 10
    if reminder > max:
        max = reminder
    num = num // 10

print("Наибольшая цифра числа равна: ", max)

print()
print("Решение Задачи-1 при помощи цикла For")
print()

num = input("Введите число: ")

max = 0

for i in num:
    k = int(i)
    if k > max:
        max = k

print("Наибольшая цифра в числе: ", max)

