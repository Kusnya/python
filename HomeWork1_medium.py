# math.sqrt(4) - вычисляет корень числа 4

# Знаю, что не стоит называть переменные одним символом, однако считаю,
# что в данном случае (для решения уравнений) использовать стандартное
# наименование переменных наиболее понятно
import math

print()
print("Задача-3: Решить уравнение.")
print()

print("Уравнение имеет вид: ax^2 + bx + c = 0")
a = int(input("Введите переменную a: "))
b = int(input("Введите переменную b: "))
c = int(input("Введите переменную c: "))

D = b**2 - 4*a*c

print()
print("Дискриминант равен: ", D, "следовательно:")

if D < 0:
    print("Уравнение не имеет решений.")
elif D == 0:
    x = (-b) / (2*a)
    print("Уравнение имеет один корень равный: ", '%.2f' % x)
else:
    x1 = (-b + math.sqrt(D)) / (2*a)
    x2 = (-b - math.sqrt(D)) / (2*a)
    print("Уравнение имеет два корня")
    print("Первый корень равен: ", '%.2f' % x1)
    print("Второй корень равен: ", '%.2f' % x2)